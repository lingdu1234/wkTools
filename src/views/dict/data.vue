<template>
  <span>{{ t("dict.dict_data") }}</span>
  <el-divider></el-divider>
  <div class="app-container">
    <el-form :model="queryParams" ref="queryRef" :inline="true" v-show="showSearch" label-width="68px">
      <el-form-item label="字典名称" prop="dictType">
        <el-select v-model="queryParams.dict_type">
          <el-option v-for="item in typeOptions" :key="item.dict_id" :label="item.dict_name" :value="item.dict_type" />
        </el-select>
      </el-form-item>
      <el-form-item label="字典标签" prop="dict_label">
        <el-input v-model="queryParams.dict_label" placeholder="请输入字典标签" clearable @keyup.enter="handleQuery" />
      </el-form-item>
      <el-form-item label="状态" prop="status">
        <el-select v-model="queryParams.status" placeholder="数据状态" clearable>
          <el-option v-for="dict in sys_normal_disable" :key="dict.value" :label="dict.label" :value="dict.value" />
        </el-select>
      </el-form-item>
      <el-form-item>
        <el-button type="primary" :icon="Search" @click="handleQuery">搜索</el-button>
        <el-button :icon="Refresh" @click="resetQuery">重置</el-button>
      </el-form-item>
    </el-form>

    <el-row :gutter="10" class="mb8" style="height: 35px">
      <el-col :span="1.5">
        <el-button type="primary" plain :icon="Plus" @click="handleAdd">新增</el-button>
      </el-col>
      <el-col :span="1.5">
        <el-button type="success" plain :icon="Edit" :disabled="single" @click="handleUpdate">修改</el-button>
      </el-col>
      <el-col :span="1.5">
        <el-button type="danger" plain :icon="Delete" :disabled="multiple" @click="handleDelete">删除</el-button>
      </el-col>
      <el-col :span="1.5">
        <el-button type="warning" plain :icon="Close" @click="handleClose">关闭</el-button>
      </el-col>
      <right-toolbar v-model:showSearch="showSearch" @queryTable="getList"></right-toolbar>
    </el-row>

    <el-table v-loading="loading" :data="dataList" @selection-change="handleSelectionChange">
      <el-table-column type="selection" width="55" align="center" />
      <el-table-column label="字典编码" align="center" prop="dict_data_id" width="100" show-overflow-tooltip />
      <el-table-column label="字典标签" align="center" prop="dict_label">
        <template #default="scope">
          <span v-if="
            scope.row.list_class == '' || scope.row.list_class == 'default'
          ">{{ scope.row.dict_label }}</span>
          <el-tag v-else :type="
            scope.row.list_class == 'primary' ? '' : scope.row.list_class
          ">{{ scope.row.dict_label }}</el-tag>
        </template>
      </el-table-column>
      <el-table-column label="字典键值" align="center" prop="dict_value" />
      <el-table-column label="字典排序" align="center" prop="dict_sort" />
      <el-table-column label="状态" align="center" prop="status">
        <template #default="scope">
          <dict-tag :options="sys_normal_disable" :value="scope.row.status" />
        </template>
      </el-table-column>
      <el-table-column label="备注" align="center" prop="remark" :show-overflow-tooltip="true" />
      <el-table-column label="操作" align="center" width="150" class-name="small-padding fixed-width">
        <template #default="scope">
          <el-link type="success" :icon="Edit" @click="handleUpdate(scope.row)">修改</el-link>
          <el-link type="danger" :icon="Delete" @click="handleDelete(scope.row)">删除</el-link>
        </template>
      </el-table-column>
    </el-table>

    <pagination v-show="total > 0" :total="total" v-model:page="queryParams.page_num"
      v-model:limit="queryParams.page_size" @pagination="getList" />

    <!-- 添加或修改参数配置对话框 -->
    <el-dialog :title="title" v-model="open" width="500px" append-to-body>
      <el-form ref="dataRef" :model="form" :rules="rules" label-width="80px">
        <el-form-item label="字典类型">
          <el-input v-model="form.dict_type" :disabled="true" />
        </el-form-item>
        <el-form-item label="数据标签" prop="dict_label">
          <el-input v-model="form.dict_label" placeholder="请输入数据标签" />
        </el-form-item>
        <el-form-item label="数据键值" prop="dict_value">
          <el-input v-model="form.dict_value" placeholder="请输入数据键值" />
        </el-form-item>
        <!-- <el-form-item label="样式属性" prop="css_class">
          <el-input v-model="form.css_class" placeholder="请输入样式属性" />
        </el-form-item> -->
        <el-form-item label="显示排序" prop="dict_sort">
          <el-input-number v-model="form.dict_sort" controls-position="right" :min="0" />
        </el-form-item>
        <el-form-item label="回显样式" prop="list_class">
          <el-select v-model="form.list_class">
            <el-option v-for="item in listClassOptions" :key="item.value" :label="item.label" :value="item.value">
            </el-option>
          </el-select>
        </el-form-item>
        <el-form-item label="状态" prop="status">
          <el-radio-group v-model="form.status">
            <el-radio v-for="dict in sys_normal_disable" :key="dict.value" :label="dict.value">{{ dict.label }}
            </el-radio>
          </el-radio-group>
        </el-form-item>
        <el-form-item label="备注" prop="remark">
          <el-input v-model="form.remark" type="textarea" placeholder="请输入内容"></el-input>
        </el-form-item>
      </el-form>
      <template #footer>
        <div class="dialog-footer">
          <el-button type="primary" @click="submitForm">确 定</el-button>
          <el-button @click="cancel">取 消</el-button>
        </div>
      </template>
    </el-dialog>
  </div>
</template>

<script setup name="Data">
import { getCurrentInstance, ref, toRefs, reactive } from 'vue';
import { useRoute } from 'vue-router';
import { invoke } from '@tauri-apps/api/tauri';

import { Plus, Edit, Delete, Search, Refresh, Close } from '@element-plus/icons-vue';
import { ElMessage, ElMessageBox } from "element-plus"
import i18n from '@/locals';


const { proxy } = getCurrentInstance();
const { sys_normal_disable } = proxy.useDict('sys_normal_disable');

const { t } = i18n.global;

const dataList = ref([]);
const open = ref(false);
const loading = ref(true);
const showSearch = ref(true);
const ids = ref([]);
const labels = ref([]);
const single = ref(true);
const multiple = ref(true);
const total = ref(0);
const title = ref('');
const defaultDictType = ref('');
const typeOptions = ref([]);
const route = useRoute();
// 数据标签回显样式
const listClassOptions = ref([
  { value: 'default', label: '默认' },
  { value: 'primary', label: '主要' },
  { value: 'success', label: '成功' },
  { value: 'info', label: '信息' },
  { value: 'warning', label: '警告' },
  { value: 'danger', label: '危险' },
]);

const data = reactive({
  form: {},
  queryParams: {
    page_num: 1,
    page_size: 10,
    dict_label: undefined,
    dict_type: undefined,
    status: undefined,
  },
  rules: {
    dict_label: [
      { required: true, message: '数据标签不能为空', trigger: 'blur' },
    ],
    dict_value: [
      { required: true, message: '数据键值不能为空', trigger: 'blur' },
    ],
    dict_sort: [
      { required: true, message: '数据顺序不能为空', trigger: 'blur' },
    ],
  },
});

const { queryParams, form, rules } = toRefs(data);

/** 查询字典类型详细 */
function getTypes(dict_type) {

  queryParams.value.dict_type = dict_type;
  defaultDictType.value = dict_type;


  getList();

}

/** 查询字典类型列表 */
async function getTypeList() {
  const qp = {
    pageParams: {
      page_num: 1,
      page_size: Number.MAX_SAFE_INTEGER
    },
    req: {}
  }
  const [data, msg] = await invoke("get_dict_type_list", qp)
  if (msg == null) {
    typeOptions.value = data.list;
  }
  else {
    ElMessage.error(msg)
  }
}
/** 查询字典数据列表 */
async function getList() {
  loading.value = true;
  const qp = {
    pageParams: {
      page_num: queryParams.value.page_num,
      page_size: queryParams.value.page_size
    },
    req: {
      dict_type: queryParams.value.dict_type,
      dict_label: queryParams.value.dict_label,
      status: queryParams.value.status,
    }
  }
  const [res, msg] = await invoke("get_dict_data_list", { ...qp })
  if (msg == null) {
    dataList.value = res.list;
    total.value = res.total;
  }
  else {
    ElMessage.error(msg)
  }
  loading.value = false;
}
/** 取消按钮 */
function cancel() {
  open.value = false;
  reset();
}
/** 表单重置 */
function reset() {
  form.value = {
    dict_data_id: undefined,
    dict_label: undefined,
    dict_value: undefined,
    css_class: undefined,
    list_class: 'default',
    dict_sort: 0,
    is_default: 'N',
    status: '1',
    remark: undefined,
  };
  proxy.resetForm('dataRef');
}
/** 搜索按钮操作 */
function handleQuery() {
  queryParams.value.page_num = 1;
  getList();
}
/** 返回按钮操作 */
function handleClose() {
  proxy.$router.back();
  // proxy.$tab.closeOpenPage();
}
/** 重置按钮操作 */
function resetQuery() {
  proxy.resetForm('queryRef');
  queryParams.value.dict_type = defaultDictType;
  handleQuery();
}
/** 新增按钮操作 */
function handleAdd() {
  reset();
  open.value = true;
  title.value = '添加字典数据';
  form.value.dict_type = queryParams.value.dict_type;
}
/** 多选框选中数据 */
function handleSelectionChange(selection) {
  ids.value = selection.map((item) => item.dict_data_id);
  labels.value = selection.map((item) => item.dict_label);
  single.value = selection.length != 1;
  multiple.value = !selection.length;
}
/** 修改按钮操作 */
async function handleUpdate(row) {
  reset();
  const dict_data_id = row.dict_data_id || ids.value[0];
  const [data, msg] = await invoke("get_dict_data_by_id", { id: dict_data_id })
  if (msg == null) {
    form.value = data;
    open.value = true;
    title.value = '修改字典数据';
  } else {
    ElMessage.error(msg)
  }

}
/** 提交按钮 */
function submitForm() {
  proxy.$refs['dataRef'].validate(async (valid) => {
    if (valid) {
      if (form.value.dict_data_id != undefined) {
        let [_res, msg] = await invoke("edit_dict_data", { req: form.value });
        if (msg == null) {
          ElMessage.success('修改成功')
          open.value = false;
          getList();
        } else {
          ElMessage.error(msg)
        }
      } else {
        let [_res, msg] = await invoke("add_dict_data", { req: form.value });
        if (msg == null) {
          ElMessage.success('新增成功')
          open.value = false;
          getList();
        } else {
          ElMessage.error(msg)
        }

      }
    }
  });
}
/** 删除按钮操作 */
function handleDelete(row) {
  const _ids = row.dict_data_id ? [row.dict_data_id] : ids.value;
  const _names = row.dict_type_id ? row.dict_label : labels.value;
  ElMessageBox
    .confirm('是否确认删除字典编号为"' + _names + '"的数据项？')
    .then(async () => {
      let [res, msg] = await invoke("delete_dict_data", { ids: _ids });
      if (msg == null) {
        ElMessage.success('删除成功')
        getList();
      } else {
        ElMessage.error(msg)
      }

    }).catch(() => { });
}

getTypes(route.params && route.params.dict_type);
getTypeList();
</script>
<style scoped>
.el-link {
  margin-right: 8px;
}
</style>